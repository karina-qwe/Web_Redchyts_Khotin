function bubbleSort(c) {
    var swapped;
    do {
        swapped = false;
        for (var i=0; i < c.length-1; i++) 
 { for (var j = 0; j < c.length-1-i; j++)
           { if (c[j+1] < c[j]) {
                var temp = c[j+1];
                c[j+1] = c[j];
                c[j] = temp;
                swapped = true;           
 }
